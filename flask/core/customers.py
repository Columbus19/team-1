
# Customer Class 
class Customer():
    def __init__(self, id=None, fName=None, lName=None, email=None, gender=None, address=None, creditScore=None):
        self.id = id
        self.fName = fName
        self.lName = lName
        self.email = email
        self.gender = gender
        self.address = address
        self.creditScore = creditScore

    